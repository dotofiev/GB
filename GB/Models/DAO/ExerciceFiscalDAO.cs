        public string id_page { get { return GB_Enum_Menu.ConfigurationBudget_ExerciceFiscal; } }
        public string context_id { get; set; }
        public long id_utilisateur { get; set; }
        public string form_combo_id { get { return "form_id_exercice_fiscal"; } }
        public string form_combo_code { get { return "form_code_exercice_fiscal"; } }
        public string form_name { get { return "exercice_fiscal"; } }
        public string form_combo_libelle { get { return "form_libelle_exercice_fiscal"; } }


        public ExerciceFiscalDAO(string context_id, long id_utilisateur)
        {
            this.context_id = context_id;
            this.id_utilisateur = id_utilisateur;
        }

        public ExerciceFiscalDAO() { }

        public void Ajouter(ExerciceFiscal obj)
        {
            try
            {
                // -- Unicité du code -- //
                if (Program.db.exercices_fiscal.Exists(l => l.code == obj.code))
                {
                    throw new GBException(App_Lang.Lang.Existing_data + " [code]");
                }

                // -- Définition de l'identifiant -- //
                obj.Crer_Id();

                // -- Mise à jour du statut -- //
                obj.statut = "O";

                // -- Mise à jour des date -- //
                obj.date_debut = GBConvert.To_DateTime(obj.date_debut).Ticks.ToString();
                obj.date_fin = GBConvert.To_DateTime(obj.date_fin).Ticks.ToString();

                // -- Vérification de la date -- //
                if (Convert.ToInt64(obj.date_debut) >= Convert.ToInt64(obj.date_fin))
                {
                    throw new GBException(App_Lang.Lang.Invalid_date + " [date_debut]");
                }

                // -- Enregistrement de la valeur -- //
                Program.db.exercices_fiscal.Add(obj);

                // -- Execution des Hubs -- //
                applicationMainHub.RechargerCombo(new ExerciceFiscalDAO());

                // -- Execution des Hubs -- //
                applicationMainHub.RechargerTable(this.id_page, this.context_id);
            }
            #region Catch
            catch (Exception ex)
            {
                // -- Vérifier la nature de l'exception -- //
                if (!GBException.Est_GBexception(ex))
                {
                    // -- Log -- //
                    GBClass.Log.Error(ex);

                    // -- Renvoyer l'exception -- //
                    throw new GBException(App_Lang.Lang.Error_message_notification);
                }
                else
                {
                    // -- Renvoyer l'exception -- //
                    throw new GBException(ex.Message);
                }
            }
            #endregion
        }

        public void Modifier(ExerciceFiscal obj)
        {
            try
            {
                // -- Unicité du code -- //
                if (Program.db.exercices_fiscal.Exists(l => l.id != obj.id && l.code == obj.code))
                {
                    throw new GBException(App_Lang.Lang.Existing_data + " [code]");
                }

                // -- Mise à jour des date -- //
                obj.date_debut = GBConvert.To_DateTime(obj.date_debut).Ticks.ToString();
                obj.date_fin = GBConvert.To_DateTime(obj.date_fin).Ticks.ToString();
                
                // -- Vérification de la date -- //
                if (Convert.ToInt64(obj.date_debut) >= Convert.ToInt64(obj.date_fin))
                {
                    throw new GBException(App_Lang.Lang.Invalid_date + " [date_debut]");
                }

                // -- Modification de la valeur -- //
                Program.db.exercices_fiscal
                    // -- Spécifier la recherche -- //
                    .Where(l => l.id == obj.id)
                    // -- Lister le résultat -- //
                    .ToList()
                    // -- Parcourir les elements résultats -- //
                    .ForEach(l =>
                    {
                        // -- Mise à jour de l'enregistrement -- //
                        l.code = obj.code;
                        l.libelle = obj.libelle;
                        l.date_debut = obj.date_debut;
                        l.date_fin = obj.date_fin;
                        l.budget_id = obj.budget_id;
                    });

                // -- Execution des Hubs -- //
                applicationMainHub.RechargerCombo(new ExerciceFiscalDAO());

                // -- Execution des Hubs -- //
                applicationMainHub.RechargerTable(this.id_page, this.context_id);
            }
            #region Catch
            catch (Exception ex)
            {
                // -- Vérifier la nature de l'exception -- //
                if (!GBException.Est_GBexception(ex))
                {
                    // -- Log -- //
                    GBClass.Log.Error(ex);

                    // -- Renvoyer l'exception -- //
                    throw new GBException(App_Lang.Lang.Error_message_notification);
                }
                else
                {
                    // -- Renvoyer l'exception -- //
                    throw new GBException(ex.Message);
                }
            }
            #endregion
        }

        public void Supprimer(List<long> ids)
        {
            try
            {
                // -- Parcours de la liste des id -- //
                ids.ForEach(id =>
                {
                    // -- Suppression des valeurs -- //
                    Program.db.exercices_fiscal.RemoveAll(l => l.id == id);
                });

                // -- Execution des Hubs -- //
                applicationMainHub.RechargerCombo(new ExerciceFiscalDAO());

                // -- Execution des Hubs -- //
                applicationMainHub.RechargerTable(this.id_page, this.context_id);
            }
            #region Catch
            catch (Exception ex)
            {
                // -- Vérifier la nature de l'exception -- //
